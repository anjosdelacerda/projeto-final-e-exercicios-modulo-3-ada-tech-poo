class MathUtils {

    static soma(a,b){
        return a + b
    }

    static subtracao(a,b){
        return a - b
    }

    static multiplicacao(a,b){
        return a * b
    }

    static divisao(a,b){
        return a / b
    }
}

console.log(MathUtils.soma(10,5))
console.log(MathUtils.subtracao(10,5))
console.log(MathUtils.multiplicacao(10,5))
console.log(MathUtils.divisao(10,5))